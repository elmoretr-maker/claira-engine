/**
 * Module orchestrator — executes a single module step in the new architecture.
 *
 * This file implements the engine-side half of the module execution model
 * described in plan.md §2 and §16.
 *
 * ── Execution flow ───────────────────────────────────────────────────────────
 *
 *   executeModuleStep(module, artifactStore, context)
 *        │
 *        ├─ 1. assertEngineContract(module)          validate contract
 *        ├─ 2. assertArtifactsAvailable(...)         enforce consumes
 *        ├─ 3. readArtifactsByType(...)              gather input data
 *        ├─ 4. module.buildPayload(artifacts)        module assembles payload
 *        ├─ 5. runClaira(kind, payload, context)     engine executes (per kind)
 *        ├─ 6. module.normalizeToArtifacts(results)  module normalizes output
 *        ├─ 7. buildArtifact(...) + writeArtifact()  store each output
 *        └─ 8. return { producedArtifacts, engineResults }
 *
 * ── Constraints ──────────────────────────────────────────────────────────────
 *   - workflowExecutor.js is NOT touched — old execute(context) path is unchanged.
 *   - Existing modules are NOT modified.
 *   - Pipelines are NEVER called directly.
 *   - The engine handler map is NEVER accessed directly — only through runClaira().
 *   - All execution goes through runClaira().
 *   - All data transfer between steps goes through artifactStore.
 *
 * ── Context shape ────────────────────────────────────────────────────────────
 *   sessionId       string         Scopes the artifact store to a user session.
 *   workflowRunId   string         Scopes the artifact store to a specific run.
 *   accountId?      string | null  Passed through to runClaira for auth/logging.
 *   rid?            string         Trace ID. Auto-generated by runClaira if absent.
 *
 *   source is always "workflow" for orchestrator calls — callers must not set it.
 *
 * ── normalizeToArtifacts contract ────────────────────────────────────────────
 *   module.normalizeToArtifacts(engineResults, context) must return an array of
 *   partial artifact field objects. The orchestrator merges in the session/run
 *   context and calls buildArtifact() to produce the full validated record.
 *
 *   Each returned object must include:
 *     artifactType     string    Semantic type token (e.g. "EntitySet").
 *     artifactVersion  number    Positive integer.
 *     moduleId         string    Must match module.id.
 *     sourceKind       string    The runClaira kind that produced the data.
 *     data             any       The artifact payload (null for marker artifacts).
 *
 *   Optional (auto-generated if absent):
 *     artifactId       string    UUID.
 *     createdAt        string    ISO 8601 timestamp.
 *
 * ── Vocabulary note ──────────────────────────────────────────────────────────
 *   module.consumes uses ARTIFACT_KINDS tokens ("entity", "event", etc.) for
 *   base contract validation. The runtime artifact store uses typed strings
 *   ("EntitySet", "EventLog", etc.). For source modules (consumes: []) this
 *   causes no issue. Reconciliation for consuming modules is deferred to the
 *   ordering system (Phase 5/6).
 */

import { assertEngineContract } from "../modules/moduleContract.js";
import { runClaira } from "../../server/runClaira.js";
import { buildArtifact } from "../pipeline/runtimeArtifactStore.js";

